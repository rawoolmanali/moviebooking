  public boolean validates()
  {
      if(txtdate.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter date");
          txtdate.grabFocus();
          return false;
      }
      if(txtscreen.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter screen");
          txtscreen.grabFocus();
          return false;
      }
      if(txtshowtime.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter showtime");
          txtshowtime.grabFocus();
          return false;
      }
      if(txtseatno.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter seatno");
          txtscreen.grabFocus();
          return false;
      }
      if(txttotalamount.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter totalamount");
          txtdate.grabFocus();
          return false;
      }
      return true;
  }
